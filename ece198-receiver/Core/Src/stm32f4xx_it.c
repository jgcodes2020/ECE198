void EXTI0_IRQHandler(void) {
    if (__HAL_GPIO_EXTI_GET_IT(GPIO_PIN_0) != RESET) {
        __HAL_GPIO_EXTI_CLEAR_IT(GPIO_PIN_0);
        intermediary_byte = (intermediary_byte << 1) | 1;
        bit_counter++;

        if (bit_counter == 8) {
            ring_buffer_put(&global_ring_buffer, intermediary_byte);
            intermediary_byte = 0;
            bit_counter = 0;
        }
    }
}

